class TestNormalization:
    """Unit tests for pure normalization helpers."""

    def test_normalize_value_comma_decimal(self):
        assert _normalize_value("3,14") == 3.14

    def test_normalize_value_float_passthrough(self):
        assert _normalize_value(42.5) == 42.5

    def test_normalize_value_int_passthrough(self):
        assert _normalize_value(10) == 10

    def test_normalize_rule_renames_field_to_indicator(self):
        r = _normalize_rule({"field": "rsi", "operator": "gt", "value": "45,5"})
        assert r["indicator"] == "rsi"
        assert "field" not in r
        assert r["operator"] == ">"
        assert r["value"] == 45.5

    def test_normalize_rule_keeps_indicator(self):
        r = _normalize_rule({"indicator": "adx", "operator": ">=", "value": 25})
        assert r["indicator"] == "adx"

    def test_normalize_signal_condition_uses_field_contract(self):
        r = _normalize_signal_condition(
            {"indicator": "volume_spike", "operator": ">=", "value": 1.2}
        )
        assert r["field"] == "volume_spike"
        assert "indicator" not in r

    def test_build_profile_config_preserves_distinct_signal_indicators(self):
        suggestion = _make_suggestion(signals={
            "logic": "AND",
            "conditions": [
                {"field": "rsi", "operator": ">=", "value": 72},
                {"field": "volume_spike", "operator": ">=", "value": 1.2},
                {"field": "orderbook_depth_usdt", "operator": ">=", "value": 20000},
            ],
        })

        config = _build_profile_config(suggestion, [], [], "SHADOW_ONLY")

        assert [c["field"] for c in config["signals"]["conditions"]] == [
            "rsi",
            "volume_spike",
            "orderbook_depth_usdt",
        ]
        assert config["entry_triggers"]["conditions"] == config["signals"]["conditions"]

    def test_check_forbidden_detects_outcome(self):
        conds = [{"field": "outcome", "operator": "=", "value": "TP_HIT"}]
        assert "outcome" in _check_forbidden(conds)

    def test_check_forbidden_detects_pnl_pct(self):
        conds = [{"indicator": "pnl_pct", "operator": ">", "value": 0.01}]
        assert "pnl_pct" in _check_forbidden(conds)

    def test_check_forbidden_allows_rsi(self):
        conds = [{"indicator": "rsi", "operator": "<", "value": 45}]
        assert _check_forbidden(conds) == []

    def test_stable_rule_id_deterministic(self):
        id1 = _stable_rule_id("rsi", "<", 45, "momentum")
        id2 = _stable_rule_id("rsi", "<", 45, "momentum")
        assert id1 == id2

    def test_stable_rule_id_prefix(self):
        rid = _stable_rule_id("rsi", "<", 45, "momentum")
        assert rid.startswith("sr_")


