def _make_db(suggestion=None, combo=None, no_suggestion=False) -> AsyncMock:
    """Build a mock AsyncSession that returns appropriate objects."""
    db = AsyncMock()

    # Track flushed/added objects
    db._added = []
    db.add = lambda obj: db._added.append(obj)
    db.flush = AsyncMock()
    db.commit = AsyncMock()

    async def _execute(q, *args, **kwargs):
        result = MagicMock()

        # Detect which model is being queried by inspecting the query string
        q_str = str(q)
        if "ProfileSuggestion" in q_str or "profile_suggestions" in q_str.lower():
            scalar_result = None if no_suggestion else (suggestion or _make_suggestion())
            result.scalars.return_value.first.return_value = scalar_result
        elif "ProfileRuleCombination" in q_str or "profile_rule_combinations" in q_str.lower():
            result.scalars.return_value.first.return_value = combo
        elif "ConfigProfile" in q_str or "config_profiles" in q_str.lower():
            # Return None to trigger creation of master score config
            result.scalars.return_value.first.return_value = None
        else:
            result.scalars.return_value.first.return_value = None

        return result

    db.execute = _execute
    return db


